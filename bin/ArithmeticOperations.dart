

main() {
  // addition
  print(10 + 2);
  //substractions
  print(10 - 2);
  // multiplication
  print(10 * 2);
  // divide
  print(10 / 3);
  // divide to return an integer
  print(10 ~/ 3);
  // mod for remainder
  print(10 % 3);
  //increment before
  var number = 0;
  var number2 = ++number;
  print(number2);
  // increment after
  var number3 = 0;
  var number4 = ++number3;
  print(number4);
  //decrement before
  var number5 = 3;
  var number6 = --number5;
  print(number6);
  //decrement after
  var number7 = 3;
  var number8 = number7--;
  print(number8);
}